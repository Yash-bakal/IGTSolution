import java.util.*;

class MaximumOf2
{	public static void main(String arg[])
	{
		Scanner sc=new Scanner(System.in);
		System.out.print("Enter A :");
		int a=sc.nextInt();
		System.out.print("Enter B :");
		int b=sc.nextInt();
		

		if(a>b)
		{
			System.out.println(+a+"  A is Greater");
		}
		else
		{
			System.out.println(+b+" B is Greater");
		}
		
			
	}
}

